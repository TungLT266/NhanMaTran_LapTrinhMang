    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String a = tfM1.getText();
        String b = tfN1.getText();
        String c = tfM2.getText();
        String d = tfN2.getText();
        
        if (!(a.isEmpty()||b.isEmpty()||c.isEmpty()||d.isEmpty())){
            m1 = Integer.parseInt(a);
            n1 = Integer.parseInt(b);
            m2 = Integer.parseInt(c);
            n2 = Integer.parseInt(d);
            if (n1 == m2) {
                send.setVisible(true);
                pMaTran1.removeAll();
                pMaTran2.removeAll();
                error.setVisible(false);
                
                tfMaTran1 = new JTextField[m1][n1];
                tfMaTran2 = new JTextField[m2][n2];

                for(int i=0; i<m1; i++){
                    for(int j=0; j<n1; j++){
                        tfMaTran1[i][j] = new JTextField("0");
                        pMaTran1.add(tfMaTran1[i][j]);
                    }
                }

                for(int i=0; i<m2; i++){
                    for(int j=0; j<n2; j++){
                        tfMaTran2[i][j] = new JTextField("0");
                        pMaTran2.add(tfMaTran2[i][j]);
                    }
                }

                pMaTran1.setLayout(new GridLayout(m1,n1));
                pMaTran1.setVisible(false);
                pMaTran1.setVisible(true);
                pMaTran2.setLayout(new GridLayout(m2,n2));
                pMaTran2.setVisible(false);
                pMaTran2.setVisible(true);
            }
            else{
                send.setVisible(false);
                error.setVisible(true);
                error.setText("Hai ma trận này không thể nhân với nhau được.");
            }
        }
        else{
            send.setVisible(false);
            error.setVisible(true);
            error.setText("Chưa nhập đủ thông tin.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

