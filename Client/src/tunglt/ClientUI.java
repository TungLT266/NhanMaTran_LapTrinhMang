    private void sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendActionPerformed
        error.setVisible(false);
        float[][] maTran1 = new float[m1][n1];
        float[][] maTran2 = new float[m2][n2];
        try{
            for(int i=0; i<m1; i++){
                for(int j=0; j<n1; j++){
                    maTran1[i][j] = Float.parseFloat(tfMaTran1[i][j].getText());
                }
            }

            for(int i=0; i<m2; i++){
                for(int j=0; j<n2; j++){
                    maTran2[i][j] = Float.parseFloat(tfMaTran2[i][j].getText());
                }
            }
            
            client.send(new Data("", maTran1, m1, n1, maTran2, m2, n2));
        }catch(Exception ex){
            error.setVisible(true);
            error.setText("Dữ liệu bạn nhập không hợp lệ");
        }
        
        
        
    }//GEN-LAST:event_sendActionPerformed

